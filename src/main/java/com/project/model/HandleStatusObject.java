package com.project.model;

public class HandleStatusObject {
	private int userCase;
	private int statusType;
	private int idInvoice;
	private int idSupplier;
	private int idInvoiceDetails;
	private int idUser;
	
	public int getUserCase() {
		return userCase;
	}
	public void setUserCase(int userCase) {
		this.userCase = userCase;
	}
	public int getStatusType() {
		return statusType;
	}
	public void setStatusType(int statusType) {
		this.statusType = statusType;
	}
	public int getIdInvoice() {
		return idInvoice;
	}
	public void setIdInvoice(int idInvoice) {
		this.idInvoice = idInvoice;
	}
	public int getIdSupplier() {
		return idSupplier;
	}
	public void setIdSupplier(int idSupplier) {
		this.idSupplier = idSupplier;
	}
	public int getIdInvoiceDetails() {
		return idInvoiceDetails;
	}
	public void setIdInvoiceDetails(int idInvoiceDetails) {
		this.idInvoiceDetails = idInvoiceDetails;
	}
	public int getIdUser() {
		return idUser;
	}
	public void setIdUser(int idUser) {
		this.idUser = idUser;
	}
	
	
	
	
}
